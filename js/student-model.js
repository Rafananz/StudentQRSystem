class Student {
    constructor(
        studentId,
        firstName,
        middleName,
        lastName,
        year,
        section,
        email,
        contact
    ) {
        this.studentId = studentId;
        this.firstName = firstName;
        this.middleName = middleName;
        this.lastName = lastName;
        this.year = year;
        this.section = section;
        this.email = email;
        this.contact = contact;
    }
}