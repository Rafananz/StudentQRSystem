class Student {
    constructor(
        studentId,
        firstName,
        middleName,
        lastName,
        year,
        section,
        email,
        emergency_contact,
        adviser,
    ) {
        this.studentId = studentId;
        this.firstName = firstName;
        this.middleName = middleName;
        this.lastName = lastName;
        this.year = year;
        this.section = section;
        this.email = email;
        this.emergency_contact = emergency_contact;
        this.adviser = adviser;
    }
}